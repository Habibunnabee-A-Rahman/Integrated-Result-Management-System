    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        //ADD Button
        String department_id = jTextField1.getText().trim();
        String department_name = jTextField2.getText().trim();
        String T03_id_fk = T03_id[schoolComboBox.getSelectedIndex()];
        //String uni_accro = jTextField4.getText().trim();
        if(department_id.isEmpty() || department_name.isEmpty() || T03_id_fk.isEmpty()){
            alertGeneration(6,"Error in Data Entry!!","Data Field is Empty!","Try Again!");
            return;
        }
        PreparedStatement ps;
        try {
            
            
            ps = connect.prepareStatement("INSERT INTO `t04_department` (`department_id`, `T03_id_fk`, `department_name`)"
                    + " VALUES (?,?,?);");
            ps.setString(1, department_id);
            ps.setString(2, T03_id_fk);
            ps.setString(3, department_name);
            ps.execute();
            
            
            alertGeneration(5,"Department Entry Successful!!","","");
            jTextField1.setText("");
            jTextField2.setText("");
            schoolComboBox.setSelectedIndex(0);
            
            
        } catch (SQLException ex) {
            //Logger.getLogger(EnterUniversityFrame.class.getName()).log(Level.SEVERE, null, ex);
            int code = ex.getErrorCode();
            if(code == 0){
                String msg = "Error Code: "+code;
                alertGeneration(3,"Error in Server Connection!!",msg,"Try Again!");
                System.out.println(ex);
            }else{
                String msg = "Error Code: "+code;
                alertGeneration(6,"Error in Data Entry!!",msg,"Try Again!");
                System.out.println(ex);
            }
            //System.out.println("ERROR!! :  "+code);
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

