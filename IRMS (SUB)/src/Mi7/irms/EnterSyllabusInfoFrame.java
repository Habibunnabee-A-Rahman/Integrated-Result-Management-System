    private void finalizeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizeButtonActionPerformed
        // TODO add your handling code here:
        ImageIcon error = new ImageIcon(getClass().getResource("/icon/error_2_64.png"));
        ImageIcon success = new ImageIcon(getClass().getResource("/icon/success_64.png"));
        if (syllabus_status) {

            JOptionPane.showMessageDialog(this, "Syllabus is Already Finalized", "Error!", JOptionPane.ERROR_MESSAGE, error);
            return;
        }
        try{
            PreparedStatement ps = connect.prepareStatement("SELECT * FROM `t06_syllabus` "
                    + "WHERE T06_id = ? AND default_T08_id_fk IS NOT NULL AND default_T08_id_fk > 0");
            ps.setString(1,idT06);
            ResultSet rs = ps.executeQuery();
            if(!rs.next()){
                JOptionPane.showMessageDialog(this, "Default Evaluation Set is not Entered", "Error!", JOptionPane.ERROR_MESSAGE, error);
                return;
            }
            
            
            ps= connect.prepareStatement("SELECT * FROM `t07_course` WHERE T06_id_fk = ?");
            ps.setString(1,idT06);
            rs = ps.executeQuery();
            if(!rs.next()){
                JOptionPane.showMessageDialog(this, "No Courses is Present!!", "Error!", JOptionPane.ERROR_MESSAGE, error);
                return;
            }
            
            
            ps= connect.prepareStatement("SELECT * FROM `t07_course` "
                    + "WHERE T06_id_fk = ? AND (course_status IS NULL OR course_status <1)");
            ps.setString(1,idT06);
            rs = ps.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "All courses are not Connected to PLO/CLO!!", "Error!", JOptionPane.ERROR_MESSAGE, error);
                return;
            }
            
            
            ps= connect.prepareStatement("UPDATE `t06_syllabus` SET `syllabus_status`= 1 "
                    + "WHERE T06_id = ?");
            ps.setString(1,idT06);
            
            if(ps.executeUpdate()>0){
                JOptionPane.showMessageDialog(this, "Syllabus is Finalized", "Success!", JOptionPane.ERROR_MESSAGE, success);
            }else{
                JOptionPane.showMessageDialog(this, "Finalization Failed", "Error!", JOptionPane.ERROR_MESSAGE, error);
            }
            
        }catch (SQLException ex) {

            Logger.getLogger(EndSemesterFrame.class.getName()).log(Level.SEVERE, null, ex);
            Logger.getLogger(EndSemesterFrame.class.getName()).log(Level.SEVERE, null, ex);
            int code = ex.getErrorCode();
            String msg = "Error Code: " + code;
            String[] options = {"OK"};
            ImageIcon icon_server_error = new ImageIcon(getClass().getResource("/icon/server_error_2_64.png"));
            ImageIcon icon_data_error = new ImageIcon(getClass().getResource("/icon/error_data_64.png"));

            if (code == 0) {
                this.reConnection();
                int select = JOptionPane.showOptionDialog(this, "Error Server Connection!!\n" + msg + "\nTry Again!", "Connection Failed!",
                        JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE, icon_server_error, options, options[0]);
                if (select == JOptionPane.CLOSED_OPTION || select == 0) {
                    this.reConnection();
                }
            } else {
                JOptionPane.showOptionDialog(this, "Error in Data Entry!!\n" + msg + "\nTry Again!", "Data Entry Failed!",
                        JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE, icon_data_error, options, options[0]);
            }
        }
    }//GEN-LAST:event_finalizeButtonActionPerformed

