    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //ADD Button
        String admin_id = jTextField1.getText().trim();
        String [] uni_id = jComboBox1.getSelectedItem().toString().split(" ", 2);
        String password = jPasswordField1.getText().trim();
        String confirm_password = jPasswordField2.getText().trim();
        //System.out.println(uni_id[0]);
        if(admin_id.isEmpty() || uni_id[0].isEmpty()){
            alertGeneration(6,"Error in Data Entry!!","Data Field is Empty!","Try Again!");
            return;
        }
        if(!password.equals(confirm_password)){
            alertGeneration(2,"Error in Password!!","Password & Confirm Password Do Not Match!","Try Again!");
            return;
        }
        PreparedStatement ps;
        password = encryptPass(password);
        try {
            
            ps = connect.prepareStatement("INSERT INTO `admin` (`ID`, `university_id`, `password`) VALUES (?, ?, ?);");
            ps.setString(1, admin_id);    
            ps.setString(2, uni_id[0]);
            ps.setString(3, password);
            ps.execute();
            
            
            alertGeneration(5,"University Admin Entry Successful!!","","");
            jTextField1.setText("");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            
            
        } catch (SQLException ex) {
            //Logger.getLogger(EnterUniversityFrame.class.getName()).log(Level.SEVERE, null, ex);
            int code = ex.getErrorCode();
            if(code == 0){
                String msg = "Error Code: "+code;
                alertGeneration(3,"Error in Server Connection!!",msg,"Try Again!");
                System.out.println(ex);
            }else{
                String msg = "Error Code: "+code;
                alertGeneration(6,"Error in Data Entry!!",msg,"Try Again!");
                System.out.println(ex);
            }
            System.out.println("ERROR!! :  "+code);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

