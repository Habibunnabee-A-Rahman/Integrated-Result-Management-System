    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        //ADD Button
        String syllabus_id = syllabusIDTextField.getText().trim();
        String syllabus_name = syllabusNameTextField.getText().trim();
        String T05_id_fk = T05_id[programComboBox.getSelectedIndex()];
        //String uni_accro = jTextField4.getText().trim();
        if(syllabus_id.isEmpty() || syllabus_name.isEmpty() || T05_id_fk.isEmpty()){
            alertGeneration(6,"Error in Data Entry!!","Data Field is Empty!","Try Again!");
            return;
        }
        PreparedStatement ps;
        try {
            
            
            ps = connect.prepareStatement("INSERT INTO `t06_syllabus` (`syllabus_id`, `T05_id_fk`, `syllabus_name`)"
                    + " VALUES (?,?,?);");
            ps.setString(1, syllabus_id);
            ps.setString(2, T05_id_fk);
            ps.setString(3, syllabus_name);
            ps.execute();
            
            
            alertGeneration(5,"Program Entry Successful!!","","");
            syllabusIDTextField.setText("");
            syllabusNameTextField.setText("");
            
            
            
        } catch (SQLException ex) {
            //Logger.getLogger(EnterUniversityFrame.class.getName()).log(Level.SEVERE, null, ex);
            int code = ex.getErrorCode();
            if(code == 0){
                String msg = "Error Code: "+code;
                alertGeneration(3,"Error in Server Connection!!",msg,"Try Again!");
                System.out.println(ex);
            }else{
                String msg = "Error Code: "+code;
                alertGeneration(6,"Error in Data Entry!!",msg,"Try Again!");
                System.out.println(ex);
            }
            //System.out.println("ERROR!! :  "+code);
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

