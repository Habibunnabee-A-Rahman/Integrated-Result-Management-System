    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //ADD Button
        String uni_id = jTextField1.getText().trim();
        String uni_name = jTextField2.getText().trim();
        String uni_accro = jTextField4.getText().trim();
        if(uni_id.isEmpty() || uni_name.isEmpty() || uni_accro.isEmpty()){
            alertGeneration(6,"Error in Data Entry!!","Data Field is Empty!","Try Again!");
            return;
        }
        PreparedStatement ps;
        try {
            
            ps = connect.prepareStatement("INSERT INTO `t01_university` (`T01_id`, `university_id`, `university_name`, `acronym`) VALUES (NULL, ?, ?, ?);");
            ps.setString(1, uni_id);
            ps.setString(2, uni_name);
            ps.setString(3, uni_accro);
            ps.execute();
            
            
            alertGeneration(5,"University Entry Successful!!","","");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField4.setText("");
            
        } catch (SQLException ex) {
            //Logger.getLogger(EnterUniversityFrame.class.getName()).log(Level.SEVERE, null, ex);
            int code = ex.getErrorCode();
            if(code == 0){
                String msg = "Error Code: "+code;
                alertGeneration(3,"Error in Server Connection!!",msg,"Try Again!");
                System.out.println(ex);
            }else{
                String msg = "Error Code: "+code;
                alertGeneration(6,"Error in Data Entry!!",msg,"Try Again!");
                System.out.println(ex);
            }
            System.out.println("ERROR!! :  "+code);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

